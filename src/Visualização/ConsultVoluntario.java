     public void limpaCampos(){
        jTextFieldCPF.setText("");
        jTextFieldEndereco.setText("");
        jTextFieldNome.setText("");
        jTextFieldPesquisa.setText("");
        jTextFieldSexo.setText("");
        jTextFieldTelefone.setText("");
    }
     
    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"Nome","CPF","Endereco","Sexo","Telefone"};
        
        bd.conecta();
        
        bd.executaSQL(Sql);
        
        try{
            bd.rst.first();
            do{
 
                dados.add(new Object[]{bd.rst.getString("nome"), bd.rst.getLong("cpf_voluntario"), bd.rst.getString("endereco"), bd.rst.getString("sexo"),bd.rst.getString("telefone")});
                
            }while(bd.rst.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane,"Erro ao Preencher ArrayList"+ex);
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableTabelaVoluntario.setModel(modelo);
        
        /*
        jTableTabelaLivros.getColumnModel().getColumn(0).setPreferredWidth(25);
        jTableTabelaLivros.getColumnModel().getColumn(1).setPreferredWidth(25);
        jTableTabelaLivros.getColumnModel().getColumn(2).setPreferredWidth(25 );
        jTableTabelaLivros.getColumnModel().getColumn(3).setPreferredWidth(25);
        */
        
        bd.desconecta();
        
    }
     
    
    
